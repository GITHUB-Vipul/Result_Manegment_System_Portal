        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branchName=(String)jComboBox2.getSelectedItem();
        String rollno=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=jTextField3.getText();
        String password=jTextField4.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","password");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branchName,name,gender,fathername,password) values('"+rollno+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+fathername+"','"+password+"')");
            JOptionPane.showMessageDialog(null,"Saved Sucessfully");
            setVisible(false);
            new adminindex2().setVisible(true);
        }
